const takeUntil = function(array, callback) {
  const output = [];
  for (let element of array) {
    if (callback(element)) {
      break;
    } else {
      output.push(element);
    }
    
  }
  return output;
};


// Test Code
assertArraysEqual(takeUntil([1, 2, 5, 7, 2, -1, 2, 4, 5], x => x < 0), [ 1, 2, 5, 7, 2 ]);
